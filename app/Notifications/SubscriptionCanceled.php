                    ->action('Notification Action', 'https://laravel.com')
                    ->line('Thank you for using our application!');
    }

    /**
     * Get the array representation of the notification.
     *
     * @param  mixed  $notifiable
     * @return array
     */
    public function toArray($notifiable)
    {
        return [
            // anything that would be appropriate for the user to see with the notification
            // if the notification is that a new lesson has been published then may include the id of the lesson or the title of the lesson or a link to it
            // or you may cast the appropriate Model to an array
            //      return $this->lesson->toArray()
            'subscription_ended' => Carbon::now()
            //
        ];
    }
}
